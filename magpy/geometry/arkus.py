"""Coordinates for Arkus cluster geometries.

This module contains the coordinates of particles arranged
into Arkus cluster geometries of varying size. Arkus clusters are
*minimum energy* clusters that represent tightly packed clusters of
spherical particles. Their definition (and name) are derived from:

N. Arkus, V. N. Manoharan, and M. P. Brenner, *Phys. Rev. Lett.* **103**, 118303 (2009).
`http://dx.doi.org/10.1103/PhysRevLett.103.118303`

Clusters of 1-5 particles have only one possible arrangement.
Clusters of 6 or more particles have a variety of different
configurations, which are accessed using a configuration id. The
available configuration ranges are:
 - 1-5 particles: [0]
 - 6 particles: [0-1]
 - 7 particles: [0-5]
 - 8 particles; [0-12]

The coordinates of the particles within the cluster are normalised
to a unit distance between each pair (where possible). The distance
between the particles can be controlled by multiplying by a scaling
factor.

Attributes:
    ARKUS (dict): a dictionary containing the geometries of Arkus clusters
        of 1-8 particles. The dictionary is accessed
        `ARKUS[n_particles][configuration_id]` and returns an `np.ndarray`
        of shape `(n_particles,3)` containing the coordinates of particles
        in the cluster.

Examples:
    .. code-block:: python

        >>> ARKUS[1][0]
        array([[0,0,0]])
        >>> ARKUS[2][0]
        array([[0,0,0],
               [0,0,1]])
        >>> ARKUS[2][0] * 1e-9 # apply a scaling factor
        array([[0,0,0],
               [0,0,1e-9]])
"""

